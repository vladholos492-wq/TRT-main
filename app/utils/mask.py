"""
Утилита для маскирования секретных значений в логах.
"""


def mask(s: str, head: int = 6, tail: int = 4) -> str:
    """
    Маскирует строку, показывая только первые и последние символы.
    
    Args:
        s: Строка для маскирования
        head: Количество символов в начале (по умолчанию 6)
        tail: Количество символов в конце (по умолчанию 4)
    
    Returns:
        Замаскированная строка или "****" если строка слишком короткая
    """
    if not s:
        return ""
    if len(s) <= head + tail:
        return "****"
    return s[:head] + "****" + s[-tail:]








